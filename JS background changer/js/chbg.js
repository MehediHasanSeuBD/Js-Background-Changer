$(document).ready(function(){
    $('#greenbtn').click(function(){
        $('body').removeClass();
        $('body').addClass('green');

    });
    $('#yellowbtn').click(function(){
        $('body').removeClass();
        $('body').addClass('yellow');

    });
    $('#orangebtn').click(function(){
        $('body').removeClass();
        $('body').addClass('orange');

    });

});